export function getSpecies(id) {
  return TREE_SPECIES[id] || TREE_SPECIES[DEFAULT_SPECIES_ID];
}

export function speciesByDifficulty(diff) {
  return Object.entries(TREE_SPECIES)
    .filter(([, meta]) => meta.difficulty === diff)
    .map(([id]) => id);
}
